package br.raul.Java.Controle;

public class FOR3 {

    public static void main(String[] args) {


         for(int i=0; i<10; i++){
             for(int j=0; j<20; j++){
                 System.out.printf("[%d | %d]", i, j);
             }
             System.out.println("\n");
         }
    }
}
